// heal.c

#include <ansi.h>

int exert(object me, object target)
{
	if( me->is_fighting() )
		return notify_fail("战斗中运功疗伤？找死吗？\n");

	if ((int)me->query_skill("shenghuo-shengong", 1) < 30)
		return notify_fail("你的圣火神功修为还不够。\n");
	if( (int)me->query("neili") < 50 ) 
		return notify_fail("你的真气不够。\n");
	if( (int)me->query("eff_qi") == (int)me->query("max_qi"))
		return notify_fail("你现在没有受任何伤。\n");

	if( (int)me->query("eff_qi") < (int)me->query("max_qi") / 7 )
		return notify_fail("你已经受伤过重，只怕一运真气便有生命危险！\n");

	write( HIW "你双手着飞焰状运起"+HIR" 圣火神功"+HIW" 疗伤。\n" NOR);
	message("vision",
		HIW + me->name() +HIW "双手着飞焰状运起"+HIR" 圣火神功 "+HIW"疗伤。\n" NOR,
		environment(me), me);

	me->receive_curing("qi", 10 + (int)me->query_skill("force")/5 );
	me->add("neili", -50);
	me->set("jiali", 0);
	return 1;
}
